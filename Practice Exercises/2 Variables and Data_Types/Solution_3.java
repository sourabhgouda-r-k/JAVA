
public class Solution_3 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 =20;
        System.out.println("Before Swaping: ");
        System.out.println("Num1: "+num1+" Num2: "+num2);
        num1 = num1+num2;
        num2= num1 -num2;
        num1 = num1-num2;
        System.out.println("After Swaping: ");
        System.out.println("Num1: "+num1+" Num2: "+num2);

    }
}
